"""Generates assets/energy_demo.png — the grid-load case study, from a real run.

Overlays the calibrated trust bound L(x_t) of a learned forecaster and of the
industry seasonal-naive baseline on the same stretch of AEP hourly load, with
the alpha-margin remedy applied (deliver 0.90). Same repo rule as every figure:
regenerated by script, information content asserted before saving.

Run: python studies/make_energy_figure.py   (~4-6 min; needs AEP_hourly.csv/)
"""

import os
import sys

import matplotlib

matplotlib.use("Agg")
import matplotlib.pyplot as plt
import numpy as np
from scipy.stats import spearmanr

sys.path.append(os.path.join(os.path.dirname(__file__), ".."))

from src.horizon_estimator import HorizonEstimator
from demo_energy import load_series  # noqa: E402  (same sorted/deduped series)

OUT = os.path.join(os.path.dirname(__file__), "..", "assets", "energy_demo.png")
SHOW_LAST = 600  # hours displayed (~25 days)


def fit(model, **kw):
    est = HorizonEstimator(
        model=model, alpha=0.085, tolerance=0.4, horizon_max=72,
        quantile_ensemble=1, mlp_epochs=40, horizon_samples=99999,
        output_dir="outputs_energy_fig", **kw,
    )
    est.fit(load_series())
    dim, lag = est.result_["dim"], est.result_["lag"]
    window_len = (dim - 1) * lag + 1
    # current-time index of window w (within the test segment)
    t = np.arange(est.lower_bounds_.size) + window_len - 1
    return est, t


def main():
    x = load_series()
    est_lin, t_lin = fit("linear")
    est_nai, t_nai = fit(lambda v: v[1], dim=25, lag=1)

    L1, H1 = est_lin.lower_bounds_, est_lin.test_horizons_
    L0 = est_nai.lower_bounds_
    rho, _ = spearmanr(L1, H1)
    print(f"learned: L_med={np.median(L1):.1f}h cov={est_lin.coverage_:.3f} "
          f"Spearman(L,H)={rho:.2f}")
    print(f"naive  : L_med={np.median(L0):.1f}h cov={est_nai.coverage_:.3f}")
    assert rho > 0.3, "figure would be decorative -- do not publish it"

    n = x.size
    i_train, i_test = int(0.6 * n), int(0.9 * n)
    mean, sd = x[:i_train].mean(), x[:i_train].std()
    test_mw = x[i_test:]

    # display window: last SHOW_LAST hours covered by both models
    t_max = min(t_lin[-1], t_nai[-1])
    t_min = t_max - SHOW_LAST
    m1 = (t_lin >= t_min) & (t_lin <= t_max)
    m0 = (t_nai >= t_min) & (t_nai <= t_max)

    bg, blue_hi, blue_lo, amber, grey = "#0b0e14", "#7fb4ff", "#1f4e8c", "#e0a458", "#3a4356"
    fig, (ax0, ax1) = plt.subplots(
        2, 1, figsize=(11, 4.8), dpi=150, sharex=True,
        gridspec_kw={"height_ratios": [1.0, 1.35], "hspace": 0.14},
    )
    fig.patch.set_facecolor(bg)
    for ax in (ax0, ax1):
        ax.set_facecolor(bg)
        for s in ax.spines.values():
            s.set_visible(False)
        ax.tick_params(colors=grey, labelsize=8)
        ax.grid(axis="y", color=grey, alpha=0.25, linewidth=0.6)

    tt = np.arange(t_min, t_max + 1)
    ax0.plot(tt, test_mw[t_min: t_max + 1] / 1000.0, color=blue_hi, linewidth=0.8)
    ax0.set_ylabel("load (GW)", color=grey, fontsize=9)

    ax1.fill_between(t_lin[m1], 0, L1[m1], step="mid", color=blue_lo, alpha=0.8,
                     label=f"learned forecaster — median {np.median(L1):.1f} h")
    ax1.plot(t_lin[m1], L1[m1], drawstyle="steps-mid", color=blue_hi, linewidth=0.9)
    ax1.plot(t_nai[m0], L0[m0], drawstyle="steps-mid", color=amber, linewidth=1.1,
             label=f"seasonal-naive baseline — median {np.median(L0):.1f} h")
    ax1.set_ylabel("guaranteed trust  L(x$_t$)  (hours)", color=grey, fontsize=9)
    ax1.set_xlabel("time (hours, held-out test — AEP hourly load)", color=grey, fontsize=9)
    ax1.set_ylim(0, max(L1[m1].max(), L0[m0].max()) + 8)
    leg = ax1.legend(loc="upper left", fontsize=8, frameon=False)
    for txt in leg.get_texts():
        txt.set_color("#c8d6f0")
    ax1.set_title(
        f"How many hours ahead can each model be trusted (error < 0.4σ ≈ 1 GW)? — "
        f"coverage {est_lin.coverage_:.2f} / {est_nai.coverage_:.2f} (target 0.90)",
        color="#c8d6f0", fontsize=9, loc="left", pad=6,
    )

    fig.savefig(OUT, bbox_inches="tight", facecolor=bg)
    print(f"saved {OUT}")


if __name__ == "__main__":
    main()
